a = 10

# Add and assign
a += 5      # a = a+5
print(a) 

# Subtract and assign
a -= 3
print(a) 

# Multiply and assign
a *= 2
print(a)  

# Divide and assign
a /= 4
print(a) 

# Modulus and assign
a %= 5
print(a)  #

# Exponentiation and assign
a **= 3
print(a) 

# Floor division and assign
a //= 2
print(a) 
