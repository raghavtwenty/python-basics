a = 10
b = 20

# Equal
print(a == b)  

# Not equal
print(a != b)  

# Greater than
print(a > b)  

# Less than
print(a < b) 

# Greater than or equal to
print(a >= b)  

# Less than or equal to
print(a <= b)  
